    hora_actual = f"{t[3]:02d}:{t[4]:02d}:{t[5]:02d}"

    # Mostrar en LCD
    lcd.clear()
    lcd.print(fecha_formato, 0, 0)
    lcd.print(hora_actual, 1, 0)

    time.sleep(1)
