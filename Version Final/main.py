def calcular_fecha_ciclo(dia):
    año, mes, dia_inicio = fecha_inicio_ciclo
    dia_simulado = dia_inicio + dia - 1
    while dia_simulado > 28:  # simplificación
        dia_simulado -= 28
        mes += 1
        if mes > 12:
            mes = 1
            año += 1
    return (año, mes, dia_simulado)

def determinar_fase(dia):
    if 1 <= dia <= 5:
        return "menstruacion"
    elif 6 <= dia <= 14:
        return "folicular"
    elif 15 <= dia <= 17:
        return "ovulacion"
    else:
        return "lutea"

def mostrar_en_oled():
    global indice_imagen, indice_consejo, mostrar_imagen_ahora, dia_ciclo
    oled.fill(0)
    
    if mostrar_imagen_ahora:
        # Mostrar imagen
        fb = framebuf.FrameBuffer(imagenes[indice_imagen], 128, 64, framebuf.MONO_HLSB)
        oled.blit(fb, 0, 0)
        indice_imagen = (indice_imagen + 1) % len(imagenes)
    else:
        # Mostrar consejo con info de ciclo
        fase = determinar_fase(dia_ciclo)
        lista_consejos = consejos[fase]

        oled.text(f"Dia {dia_ciclo}", 0, 0)
        oled.text(f"Etapa: {fase}", 0, 10)
        oled.text("Consejo:", 0, 20)

        # Mostrar hasta 3 consejos desde el índice actual
        for i in range(3):
            texto = lista_consejos[(indice_consejo + i) % len(lista_consejos)]
            oled.text(texto, 0, 30 + i * 10)

        indice_consejo += 1
        dia_ciclo = dia_ciclo + 1 if dia_ciclo < 28 else 1

    oled.show()
    mostrar_imagen_ahora = not mostrar_imagen_ahora

# Temporizador OLED (5 segundos)
